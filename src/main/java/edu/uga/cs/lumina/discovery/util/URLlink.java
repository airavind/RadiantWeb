package edu.uga.cs.lumina.discovery.util;

public class URLlink {
		
	private String name = "";
	private String wsLocation = "";
	private String backupWSDLURL = "";	
	private String SAWSDLURL = "";
	private String WSDLmessage = "";
	private String SAWSDLmessage = "";
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
	
	public void setWsLocation(String wsLocation) {
		this.wsLocation = wsLocation;
	}
	public String getWsLocation() {
		return wsLocation;
	}
	
	public void setBackupWSDLURL(String backupWSDLURL) {
		this.backupWSDLURL = backupWSDLURL;
	}
	public String getBackupWSDLURL() {
		return backupWSDLURL;
	}
	
	public void setSAWSDLURL(String sAWSDLURL) {
		SAWSDLURL = sAWSDLURL;
	}
	public String getSAWSDLURL() {
		return SAWSDLURL;
	}
	public void setWSDLmessage(String wSDLmessage) {
		WSDLmessage = wSDLmessage;
	}
	public String getWSDLmessage() {
		return WSDLmessage;
	}
	public void setSAWSDLmessage(String sAWSDLmessage) {
		SAWSDLmessage = sAWSDLmessage;
	}
	public String getSAWSDLmessage() {
		return SAWSDLmessage;
	}
	
		
}
	


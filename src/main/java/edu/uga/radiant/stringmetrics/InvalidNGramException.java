package edu.uga.radiant.stringmetrics;



public class InvalidNGramException extends Exception {

	public InvalidNGramException() {
		super();
	}

	public InvalidNGramException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidNGramException(String message) {
		super(message);
	}

	public InvalidNGramException(Throwable cause) {
		super(cause);
	}

}